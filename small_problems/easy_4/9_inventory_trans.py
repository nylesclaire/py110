### Problem
# inputs: 
#   1) inventory item ID number
#   2) list of transactions where each transaction is a dict
# outputs:
#   1) list of transactions (dicts) for only the specified number

def transactions_for(id_num, transactions_dict):
    return [trans_subdict 
            for trans_subdict in transactions_dict 
            if trans_subdict['id'] == id_num
    ]

# Test case
